#include "libft.h"

void		ft_putnbr(int n)
{
	int neg;

	neg = -1;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * neg;
	}
	if (n / 10 > 0)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}
